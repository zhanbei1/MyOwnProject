package beans;

/**
 *
 * @author 展贝
 */
public class Clock {
    private String id;//员工编号
    private String name;//员工姓名
    private String status;//员工状态，只有上班（OnWorking）/下班（OutWorking）这两种状态

    public Clock() {
    }

    public Clock(String id, String name, String status) {
        this.id = id;
        this.name = name;
        this.status = status;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    
}
